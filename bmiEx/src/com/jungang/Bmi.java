package com.jungang;

public class Bmi {

 public static void main(String[] args) {
	/**
	 * 	 * bmi구하기
	 */
	 //변수 선언
	 // 타입 변수이름;
	 int weight = 50;
	 double height = 1.6;
	 double bmi;
	 //변수에 연산식의 결과를 저장 ㄴㄱ
	 bmi = weight/(height*height);
	 
	 System.out.println("당신의 BMI "+ bmi + "입니다");
	 
	 

	
}

	}


