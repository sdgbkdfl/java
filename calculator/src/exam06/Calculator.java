package exam06;

public class Calculator {

	//정사각형의 넓이를 구하는 메소드
	
	/**
	 * 정사각형의 넓이 구하기
	 * @param width
	 * @return width*width
	 */
	
	double areaRectangle(double width) {
		return width*width;
		
		
	}
   //직사각형의 넓이 구하기
	
	/**
	 * 직사각형의 넓이 구하기
	 * @param width, height
	 * @return width*height
	 */
	double areaRectangle(double width, double height) {
		return width*height;
		
	}
}
