package looping;

public class ForEx {

	public static void main(String[] args) {
		//for(초기화식; 조건식; 증감식){}
		//구구단 ex) 2x1=2
		
		
		for (int m=2;m<=9; m++) {
			System.out.println(m +"단");
			
		for (int n=1; n<=9;n++) {
			System.out.println(m+" x "+n+" = "+(m*n));
			
			
		}
		}

	}

}
